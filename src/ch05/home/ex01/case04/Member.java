package ch05.home.ex01.case04;

public class Member {
	private String name;
	private int age;
	private String memberDate;
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return this.name;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public int getAge() {
		return this.age;
	}
	
	public void setMemberDate(String date) {
		this.memberDate = date;
	}
	
	public String getMemberDate() {
		return this.memberDate;
	}
}
